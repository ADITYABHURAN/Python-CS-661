# Part B: Expressions

# Define variables
weight = 69   # in kg
height = 1.78 # in meters

# Calculate BMI
bmi = weight / (height ** 2)

# Print BMI with descriptive message
print("Your Body Mass Index (BMI) is:", bmi)

# Using floor division (//) and modulus (%) operators
num1 = 23
num2 = 5

floor_div_result = num1 // num2  # Floor division
modulus_result = num1 % num2     # Modulus (remainder)

print("Floor division (23 // 5):", floor_div_result)
print("Modulus (23 % 5):", modulus_result)

# Explanation:
# The // operator performs floor division, which divides two numbers and 
# gives the largest integer less than or equal to the result. (23 // 5 = 4)
# The % operator returns the remainder left after division. (23 % 5 = 3)
