# Part A: Variables & Types

# Creating variables
full_name = "Aditya Bhuran"   # string
age = 23                      # integer
height = 1.78                 # float

# Printing variables with their types
print(full_name, type(full_name))
print(age, type(age))
print(height, type(height))

# Explanation:
# In Python, the type of a variable is decided automatically at runtime 
# based on the value assigned to it. This is called "dynamic typing".
# For example:
# If you assign text in quotes, Python treats it as a string.
# If you assign a whole number, it becomes an integer.
# If you assign a number with a decimal, it becomes a float.
